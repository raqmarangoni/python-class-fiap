listaDeInstrumentos = ["Piano", "Guitarra", "Bateria"]

print(type(listaDeInstrumentos)) # <class 'list'>
print(listaDeInstrumentos[0]) # Piano
# Um exemplo com loop for para exibir todos os instrumentos da lista:
for instrumento in listaDeInstrumentos:
    print(instrumento) # Piano Guitarra Bateria